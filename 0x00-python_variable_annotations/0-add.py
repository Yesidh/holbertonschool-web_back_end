#!/usr/bin/env python3
"""
===============================================================================
type-annotated function add that takes a float a and a float b as
arguments and returns their sum as a float.
===============================================================================
"""


def add(a: float = 0, b: float = 0) -> float:
    """
        :param a: first number
        :param b: second number
        :return: the sum a + b
    """
    return a + b
